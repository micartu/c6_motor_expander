static void cmd_to_motor(enum motor_side_t side, uint16_t torque, uint8_t action)
{
  enum motor_state_t maction;
  switch (action)
  {
    case 0:
    maction = FORWARD;
    break;

    case 1:
    maction = BACKWARD;
    break;

    case 2:
    maction = IDLE;
    break;

    default:
    maction = BRAKE;
    break;
  }
  apply_action_to_motor(&motor, side, maction, torque);
}

static void usb_command_received(uint8_t cmd, const void *buf, size_t sz)
{
  switch (cmd)
  {
  case CMD_MOTOR_PWM:
  {
    const size_t u16sz = sizeof(uint16_t);
    const size_t u8sz = sizeof(uint8_t);
    if (sz >= 2 * (u16sz + u8sz))
    {
      uint16_t m0;
      uint8_t m0cmd;
      uint16_t m1;
      uint8_t m1cmd;
      size_t ptr = 0;
      memcpy(&m0, buf + ptr, u16sz);
      ptr += u16sz;
      memcpy(&m0cmd, buf + ptr, u8sz);
      ptr += u8sz;
      memcpy(&m1, buf + ptr, u16sz);
      ptr += u16sz;
      memcpy(&m1cmd, buf + ptr, u8sz);
      strcpy(msg, "ok");
      CDC_Transmit_FS((uint8_t *)msg, strlen(msg));

      cmd_to_motor(MLEFT, m0, m0cmd);
      cmd_to_motor(MRIGHT, m1, m1cmd);
    }
    else
    {
      snprintf(msg, sizeof(msg), "wrong size: %ld\r", (long)sz);
      CDC_Transmit_FS((uint8_t *)msg, strlen(msg));
    }
  }
  break;
  }
}

void ext_receive_usb(uint8_t *buf, uint32_t len)
{
  uart_over_usb_data_received_handler(&uart, buf, len);
}

static void send_data_over_UART(uint32_t data)
{
  if (data <= CUSTOM_PING)
  {
    uint16_t tor;
    if (data == CUSTOM_PING) {
      tor = 65535 - 100;
    } else {
      tor = 0;
    }
    cmd_to_motor(MLEFT, tor, tor > 0 ? 0 : 2);
    cmd_to_motor(MRIGHT, tor, tor > 0 ? 0 : 2);
    HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
    snprintf(msg, sizeof(msg), "%ld\r", (long)data);
    CDC_Transmit_FS((uint8_t *)msg, sizeof(msg));
  }
  else
  {
    snprintf(msg, sizeof(msg),"unknown sub-command %ld\r", (long)data);
    CDC_Transmit_FS((uint8_t *)msg, sizeof(msg));
  }
}

